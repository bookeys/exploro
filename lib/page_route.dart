import 'package:flutter/material.dart';

class MyCustomRoute<T> extends PageRouteBuilder<T> {
  MyCustomRoute({required builder}) : super(pageBuilder: builder);

  @override
  Widget buildTransitions(BuildContext context, Animation<double> animation,
      Animation<double> secondaryAnimation, Widget child) {
    return FadeTransition(opacity: animation, child: child);
  }
}

class HorizontalSlideRoute<T> extends PageRouteBuilder<T> {
  HorizontalSlideRoute({required builder}) : super(pageBuilder: builder);

  @override
  Widget buildTransitions(BuildContext context, Animation<double> animation,
      Animation<double> secondaryAnimation, Widget child) {
    const begin = Offset(1.0, 0.0);
    const end = Offset.zero;
    const curve = Curves.easeInOutCubic;

    var tween = Tween(begin: begin, end: end).chain(CurveTween(curve: curve));

    return SlideTransition(
      position: animation.drive(tween),
      child: child,
    );
  }
}
